          '6' => '-....',
          '7' => '--...',
          '8' => '---..',
          '9' => '----.',
          '0' => '----'
          }

MORSE.default=" | "  

def translate2morse(str)  
  str.downcase.split("").map{|c|MORSE[c]}.join(" ")  
end  

